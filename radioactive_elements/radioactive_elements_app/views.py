def getElementFromId(id):
    for element in data['elements']:
        if element['id'] == id:
            return element

def getElementsFromDecayId(id):
    elements = []
    for decay in data['decays']:
        if decay['id'] == id:
            for element in decay['elements']:
                elementFromId = getElementFromId(element['id'])
                elementFromId.update({'quantity': element['quantity']})
                elements.append(elementFromId)
            return elements

def getPassTimeFromDecayId(id):
    for decay in data['decays']:
        if decay['id'] == id:
            return decay['pass_time']

def getService(request, id):
    return render(request, 'service.html', {'element': getElementFromId(id)})

def basketCircleController(id):
    for decay in data['decays']:
        if decay['id'] == id:
            return len(decay['elements'])

def getServices(request):
    data['searchResult'].clear()
    data['searchNum'] = request.GET.get('atomic_mass', '')
    for element in data['elements']:
        if data['searchNum'] in element['atomic_mass']:
            data['searchResult'].append(element)

    return render(request, 'services.html', {'elements': data['searchResult'], 'basketCount': basketCircleController(data['decayId']), 'decayId': data['decayId']})

